    private void PilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PilihActionPerformed
        FileFilter filter = new FileNameExtensionFilter("MP3 Files", "mp3");
        
        JFileChooser chooser = new JFileChooser("C:\\Music");
        chooser.addChoosableFileFilter(filter);
        
        int returnVal = chooser.showOpenDialog(null);
        
        if(returnVal == JFileChooser.APPROVE_OPTION){
            Mp3Klas.Stop();
            File myFile = chooser.getSelectedFile();
            String song = myFile + "";
            
            String name = chooser.getSelectedFile().getName();
            Path.setText(name);
            Path.setEnabled(true);
            
            try {
                Mp3Klas.Play(song);
            } catch (UnsupportedAudioFileException | LineUnavailableException ex) {
                JOptionPane.showMessageDialog(null, "File tidak bisa dibuka!");
            }
            Pinta.setText("Lagu sedang diputar...");
            Mulai.setEnabled(false);
            Jeda.setEnabled(true);
            Mati.setEnabled(true);
            Ulang.setEnabled(true);
            Pilih.setEnabled(false);
            Pilih2.setEnabled(false);
        }
    }//GEN-LAST:event_PilihActionPerformed

